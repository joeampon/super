# Figure 4 — Revenue breakdown by product group (stacked bar)
# ============================================================================
def fig4_revenue_breakdown():
    """Stacked bar chart of annual revenue by product group × scenario."""
    # Data from documented optimal_scenarios.md results
    scenarios = ["baseline", "high_fuel", "high_chem", "high_organics"]
    labels = ["Baseline", "High fuel", "High chem.", "High organics"]
    groups = ["Fuels", "Chemicals", "Organics", "Hydrogen"]
    data = {
        "Fuels":     [18.68, 27.37, 10.16,  6.16],
        "Chemicals": [ 5.96,  2.91,  7.41,  0.54],
        "Organics":  [16.57, 16.26, 16.19, 92.49],
        "Hydrogen":  [ 4.12,  1.65,  1.65,  1.65],
    }
    colors_bar = [COLORS[0], COLORS[1], COLORS[2], COLORS[3]]

    x = np.arange(len(scenarios))
    width = 0.6

    fig, ax = plt.subplots(figsize=figsize("single"))
    bottom = np.zeros(len(scenarios))
    for j, grp in enumerate(groups):
        vals = np.array(data[grp])
        bars = ax.bar(x, vals, width, bottom=bottom, label=grp,
                       color=colors_bar[j], edgecolor="white", linewidth=0.4)
        # Label bars > $5 M
        for k, (v, b) in enumerate(zip(vals, bottom)):
            if v > 5:
                ax.text(x[k], b + v / 2, f"${v:.0f}M",
                        ha="center", va="center", fontsize=5.5, color="white",
                        fontweight="bold")
        bottom += vals

    ax.set_xticks(x)
    ax.set_xticklabels(labels, fontsize=7)
    ax.set_ylabel("Annual revenue ($ M yr⁻¹)")
    ax.legend(loc="upper left", fontsize=6, ncol=2)

    savefig(fig, os.path.join(_FIG_DIR, "fig4_revenue_breakdown"))
    print("  → fig4_revenue_breakdown.pdf / .png")
    plt.close(fig)


