# Figure 6 — Literature comparison (MSP and GWP vs published studies)
# ============================================================================
def fig6_literature_comparison():
    """Side-by-side comparison of this work vs literature values.

    Literature MSP and GWP data from published waste-plastic pyrolysis
    TEA/LCA studies cited in the manuscript.
    """
    # ── Literature data ────────────────────────────────────────────────
    # Source                                    MSP ($/kg feed)   GWP (kg CO2-eq/kg feed)
    # Dang et al. 2016  [10]                    +0.12             –0.18
    # Westerhout et al. 1998  [11]              +0.25             n/a
    # Yadav et al. 2022  [18]                   +0.08             –0.12
    # Jeswani et al. 2021  [19]                 n/a               –0.28
    # Meys et al. 2021  [20]                    n/a               –0.10
    lit = [
        {"label": "Dang et al.\n(2016)",       "MSP":  0.12, "GWP": -0.18},
        {"label": "Westerhout et al.\n(1998)", "MSP":  0.25, "GWP":  np.nan},
        {"label": "Yadav et al.\n(2022)",      "MSP":  0.08, "GWP": -0.12},
        {"label": "Jeswani et al.\n(2021)",    "MSP":  np.nan, "GWP": -0.28},
        {"label": "Meys et al.\n(2021)",       "MSP":  np.nan, "GWP": -0.10},
    ]

    # ── This work (documented optimal results) ────────────────────────
    this_work = [
        {"label": "This work\n(baseline)",       "MSP": -0.524, "GWP": -0.315},
        {"label": "This work\n(high organics)",  "MSP":  0.009, "GWP": -0.876},
    ]

    # ── Panel (a): MSP comparison ─────────────────────────────────────
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=figsize("double", aspect=0.45))

    # Collect MSP entries
    msp_labels, msp_vals, msp_colors = [], [], []
    for d in lit:
        if np.isfinite(d["MSP"]):
            msp_labels.append(d["label"])
            msp_vals.append(d["MSP"])
            msp_colors.append(COLORS[0])
    for d in this_work:
        if np.isfinite(d["MSP"]):
            msp_labels.append(d["label"])
            msp_vals.append(d["MSP"])
            msp_colors.append(COLORS[1])

    y_pos = np.arange(len(msp_labels))
    bars1 = ax1.barh(y_pos, msp_vals, color=msp_colors, edgecolor="white",
                      linewidth=0.4, height=0.6)
    ax1.set_yticks(y_pos)
    ax1.set_yticklabels(msp_labels, fontsize=6.5)
    ax1.set_xlabel("MSP ($ kg⁻¹ feed)")
    ax1.axvline(0, color="k", lw=0.5, ls="-")
    ax1.invert_yaxis()
    # Value labels
    for bar, val in zip(bars1, msp_vals):
        offset = 0.02 if val >= 0 else -0.02
        ha = "left" if val >= 0 else "right"
        ax1.text(val + offset, bar.get_y() + bar.get_height() / 2,
                 f"{val:+.3f}", va="center", ha=ha, fontsize=6)

    # ── Panel (b): GWP comparison ─────────────────────────────────────
    gwp_labels, gwp_vals, gwp_colors = [], [], []
    for d in lit:
        if np.isfinite(d["GWP"]):
            gwp_labels.append(d["label"])
            gwp_vals.append(d["GWP"])
            gwp_colors.append(COLORS[0])
    for d in this_work:
        if np.isfinite(d["GWP"]):
            gwp_labels.append(d["label"])
            gwp_vals.append(d["GWP"])
            gwp_colors.append(COLORS[1])

    y_pos2 = np.arange(len(gwp_labels))
    bars2 = ax2.barh(y_pos2, gwp_vals, color=gwp_colors, edgecolor="white",
                      linewidth=0.4, height=0.6)
    ax2.set_yticks(y_pos2)
    ax2.set_yticklabels(gwp_labels, fontsize=6.5)
    ax2.set_xlabel(r"GWP (kg CO$_2$-eq kg⁻¹ feed)")
    ax2.axvline(0, color="k", lw=0.5, ls="-")
    ax2.invert_yaxis()
    for bar, val in zip(bars2, gwp_vals):
        offset = 0.02 if val >= 0 else -0.02
        ha = "left" if val >= 0 else "right"
        ax2.text(val + offset, bar.get_y() + bar.get_height() / 2,
                 f"{val:.3f}", va="center", ha=ha, fontsize=6)

    # Legend
    from matplotlib.patches import Patch
    legend_elements = [
        Patch(facecolor=COLORS[0], edgecolor="white", label="Literature"),
        Patch(facecolor=COLORS[1], edgecolor="white", label="This work"),
    ]
    ax1.legend(handles=legend_elements, loc="lower right", fontsize=6)

    label_panels([ax1, ax2])
    fig.tight_layout()
    savefig(fig, os.path.join(_FIG_DIR, "fig6_literature_comparison"))
    print("  → fig6_literature_comparison.pdf / .png")
    plt.close(fig)


