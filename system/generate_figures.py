# Figure 5 — Sensitivity contour plots (6 key pairs × 3 objectives)
# ============================================================================
def fig5_contours():
    """Contour subplots from pre-computed contour_results.pkl."""
    import pickle

    pkl_path = os.path.join(_PROJECT_ROOT, "contour_results.pkl")
    if not os.path.exists(pkl_path):
        print("  [SKIP] contour_results.pkl not found — run system.contours first.")
        return

    with open(pkl_path, "rb") as f:
        results = pickle.load(f)

    axis_labels = {
        "split_TOD": "CP + TOD fraction (x₁)",
        "split_CP": "TOD fraction (x₂)",
        "split_CPY": "CPY fraction (x₃)",
        "split_PLASMA": "PLASMA fraction",
        "split_HC": "HC fraction (x₄)",
    }

    # Select 6 key pairs (drop redundant PLASMA pairs since PLASMA = 1-CPY)
    key_pairs = [
        "split_TOD vs split_CP",
        "split_TOD vs split_CPY",
        "split_TOD vs split_HC",
        "split_CP vs split_CPY",
        "split_CP vs split_HC",
        "split_CPY vs split_HC",
    ]
    pairs = {k: results[k] for k in key_pairs if k in results}
    n_pairs = len(pairs)

    fig, axes = plt.subplots(n_pairs, 3, figsize=(7.0, 2.2 * n_pairs))
    if n_pairs == 1:
        axes = axes[np.newaxis, :]

    for row, (key, data) in enumerate(pairs.items()):
        X, Y = np.meshgrid(data["x_vals"], data["y_vals"])
        x_label = axis_labels.get(data["x_name"], data["x_name"])
        y_label = axis_labels.get(data["y_name"], data["y_name"])

        # MSP
        ax = axes[row, 0]
        cf = ax.contourf(X, Y, data["MSP"], levels=15, cmap=CMAP_DIV)
        ax.contour(X, Y, data["MSP"], levels=15, colors="k", linewidths=0.4)
        fig.colorbar(cf, ax=ax, label="MSP ($ kg⁻¹ feed)")
        ax.set_xlabel(x_label)
        ax.set_ylabel(y_label)
        if row == 0:
            ax.set_title("MSP", fontsize=8)

        # GWP
        ax = axes[row, 1]
        cf = ax.contourf(X, Y, data["GWP"], levels=15, cmap=CMAP_SEQ)
        ax.contour(X, Y, data["GWP"], levels=15, colors="k", linewidths=0.4)
        fig.colorbar(cf, ax=ax, label=r"GWP (kg CO$_2$-eq kg⁻¹)")
        ax.set_xlabel(x_label)
        ax.set_ylabel(y_label)
        if row == 0:
            ax.set_title("GWP", fontsize=8)

        # CAC
        ax = axes[row, 2]
        cac = data["CAC"]
        if np.all(np.isnan(cac)):
            ax.text(0.5, 0.5, "No finite\nCAC values", transform=ax.transAxes,
                    ha="center", va="center", fontsize=7)
        else:
            cac_f = cac[np.isfinite(cac)]
            has_neg = np.any(cac_f < 0)
            if has_neg:
                abs_max = np.nanmax(np.abs(cac_f))
                abs_pos = cac_f[cac_f > 0]
                linthresh = np.nanmin(abs_pos) if len(abs_pos) > 0 else 1.0
                norm = SymLogNorm(linthresh=linthresh, vmin=-abs_max, vmax=abs_max)
                cf = ax.contourf(X, Y, cac, levels=15, cmap="RdBu_r", norm=norm)
                ax.contour(X, Y, cac, levels=15, colors="k", linewidths=0.4,
                           norm=norm)
            else:
                vmin = np.nanmin(cac_f[cac_f > 0])
                vmax = np.nanmax(cac_f)
                norm = LogNorm(vmin=vmin, vmax=vmax)
                cf = ax.contourf(X, Y, cac, levels=np.geomspace(vmin, vmax, 15),
                                 cmap="plasma", norm=norm)
                ax.contour(X, Y, cac, levels=np.geomspace(vmin, vmax, 15),
                           colors="k", linewidths=0.4, norm=norm)
            fig.colorbar(cf, ax=ax, label=r"CAC ($ kg⁻¹ CO$_2$-eq)")
        ax.set_xlabel(x_label)
        ax.set_ylabel(y_label)
        if row == 0:
            ax.set_title("CAC", fontsize=8)

    label_panels(axes.ravel())
    fig.tight_layout()
    savefig(fig, os.path.join(_FIG_DIR, "fig5_contours"))
    print("  → fig5_contours.pdf / .png")
    plt.close(fig)


